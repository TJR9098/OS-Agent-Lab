#pragma once

#include <stdint.h>

#define DRAM_BASE 0x80000000UL
#define KERNEL_PHYS_BASE 0x80200000UL
#define KERNEL_VIRT_BASE 0xffffffff80000000UL
#define KERNEL_VIRT_OFFSET (KERNEL_VIRT_BASE - KERNEL_PHYS_BASE)
#define PAGE_OFFSET 0xffffffc000000000UL

#define USER_BASE 0x0000000000400000UL
#define USER_LIMIT (USER_BASE + 0x01000000UL)
#define USER_STACK_TOP USER_LIMIT
#define USER_STACK_SIZE (512UL * 1024)
#define USER_STACK_BASE (USER_STACK_TOP - USER_STACK_SIZE)

#define UART0_BASE 0x10000000UL
#define CLINT_BASE 0x02000000UL
#define PLIC_BASE 0x0c000000UL
#define VIRTIO0_BASE 0x10001000UL

#define PAGE_SIZE 4096UL
#define SZ_2M 0x200000UL
#define SZ_1G 0x40000000UL

#define KSTACK_PAGES 2UL
#define KSTACK_SIZE (KSTACK_PAGES * PAGE_SIZE)

